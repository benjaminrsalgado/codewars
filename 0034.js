// Very simple, given an integer or a floating-point number, find its opposite.

function opposite(number) {
    return -(number);
  }


  /*
  return -(number): Lo que hace es tomar el número pasado como argumento (number) y negarlo usando el operador de negación -. El operador - antes de number cambia el signo del número. Por ejemplo, si number es 5, la expresión -5 devolverá -5, y si number es -3, la expresión -(-3) devolverá 3*/